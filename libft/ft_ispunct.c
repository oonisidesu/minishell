//  041 ``!'' 042 ``"'' 043 ``#'' 044 ``$'' 045 ``%''
//  046 ``&'' 047 ``''' 050 ``('' 051 ``)'' 052 ``*''
//  053 ``+'' 054 ``,'' 055 ``-'' 056 ``.'' 057 ``/''
//  072 ``:'' 073 ``;'' 074 ``<'' 075 ``='' 076 ``>''
//  077 ``?'' 100 ``@'' 133 ``['' 134 ``\'' 135 ``]''
//  136 ``^'' 137 ``_'' 140 ```'' 173 ``{'' 174 ``|''
//  175 ``}'' 176 ``~''

int	ft_ispunct(int c)
{
	return ((041 <= c && c <= 057) || (072 <= c && c <= 0100) || (0133 <= c
			&& c <= 0140) || (0173 <= c && c <= 0176));
}
